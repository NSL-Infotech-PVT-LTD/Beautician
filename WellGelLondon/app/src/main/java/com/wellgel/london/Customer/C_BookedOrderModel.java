package com.wellgel.london.Customer;

public class C_BookedOrderModel {

    String bookedDate;
    String bookedSalon;
    String bookedSalonAdd;

    public String getBookedDate() {
        return bookedDate;
    }

    public void setBookedDate(String bookedDate) {
        this.bookedDate = bookedDate;
    }

    public String getBookedSalon() {
        return bookedSalon;
    }

    public void setBookedSalon(String bookedSalon) {
        this.bookedSalon = bookedSalon;
    }

    public String getBookedSalonAdd() {
        return bookedSalonAdd;
    }

    public void setBookedSalonAdd(String bookedSalonAdd) {
        this.bookedSalonAdd = bookedSalonAdd;
    }
}
